const totals = initStats();
const chapterStats = {};
for (const ch of chapters) chapterStats[String(ch.id)] = initStats();

for (const c of chaptersPayload) {
  for (const q of c.questions) {
    const s =
      chapterStats[String(c.chapter)] ||
      (chapterStats[String(c.chapter)] = initStats());
    s.all++;
    totals.all++;

    if (q.type === "mc") {
      s.mc++;
      totals.mc++;
      if (
        q.difficulty === "easy" ||
        q.difficulty === "medium" ||
        q.difficulty === "hard"
      ) {
        s[q.difficulty]++;
        totals[q.difficulty]++;
        const k = `mc_${q.difficulty}`;
        s[k]++;
        totals[k]++;
      }
    } else if (q.type === "tf") {
      s.tf++;
      totals.tf++;
    } else if (q.type === "sa") {
      s.sa++;
      totals.sa++;
    }
  }
}

