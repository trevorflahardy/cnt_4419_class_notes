import fs from "fs";
import path from "path";

const workspaceRoot =
  "/Users/futur/Library/Mobile Documents/com~apple~CloudDocs/School/USF/Spring 2026/CNT 4419 Secure Coding/class_notes";
const publicDir = path.join(workspaceRoot, "site/public");
const chapterDir = path.join(publicDir, "question-bank-chapters");
const indexPath = path.join(publicDir, "question-bank-index.json");

const chapterFiles = fs
  .readdirSync(chapterDir)
  .filter((f) => /^chapter-\d+\.json$/.test(f))
  .sort((a, b) => a.localeCompare(b));

if (!chapterFiles.length) throw new Error("No chapter files found");

const chaptersPayload = chapterFiles.map((f) => {
  const p = path.join(chapterDir, f);
  const data = JSON.parse(fs.readFileSync(p, "utf8"));
  return {
    file: f,
    path: p,
    chapter: Number(data.chapter),
    version: Number(data.version || 2),
    questions: Array.isArray(data.questions) ? data.questions : [],
  };
});

const allQuestions = chaptersPayload.flatMap((c) => c.questions);
const existingIds = new Set(allQuestions.map((q) => q.id));
let nextId = Math.max(0, ...allQuestions.map((q) => q.id || 0)) + 1;
const newQuestions = [];

function nextUniqueId() {
  while (existingIds.has(nextId)) nextId++;
  existingIds.add(nextId);
  return nextId++;
}

function addMC(chapter, difficulty, question, options, answer, explanation) {
  newQuestions.push({
    id: nextUniqueId(),
    chapter,
    type: "mc",
    difficulty,
    question,
    options,
    answer,
    explanation,
  });
}

function addTF(chapter, question, answer, explanation) {
  newQuestions.push({
    id: nextUniqueId(),
    chapter,
    type: "tf",
    question,
    answer,
    explanation,
  });
}

function addSA(chapter, question, answer) {
  newQuestions.push({
    id: nextUniqueId(),
    chapter,
    type: "sa",
    question,
    answer,
  });
}

